int	ft_sqrt(int nb)
{
	long		i;

	i = 1;
	if (nb == 1 || nb == 0)
	{
		return (nb);
	}
	while (i <= (nb / i))
	{
		if (nb == i * i)
		{
			return (i);
		}
		i++;
	}
	return (0);
}
